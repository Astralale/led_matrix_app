const int kW = 32;
const int kH = 16;

List<List<int>> normalize32x16(List<List<int>> m) {
  final out = List.generate(kH, (_) => List.filled(kW, 0));
  for (int y = 0; y < kH && y < m.length; y++) {
    for (int x = 0; x < kW && x < m[y].length; x++) {
      final v = m[y][x];
      out[y][x] = (v < 0 || v > 15) ? 0 : v;
    }
  }
  return out;
}

